CREATE TABLE bank_customer_data (
    customer_id SERIAL PRIMARY KEY,
    first_name TEXT,
    last_name TEXT,
    age INT,
    gender VARCHAR(10),
    address TEXT,
    city TEXT,
    contact_number VARCHAR(20),
    email TEXT,
    account_type VARCHAR(50),
    account_balance NUMERIC(15,2),
    date_of_account_opening DATE,
    last_transaction_date DATE,
    transaction_id TEXT,
    transaction_date DATE,
    transaction_type VARCHAR(50),
    transaction_amount NUMERIC(15,2),
    account_balance_after_transaction NUMERIC(15,2),
    branch_id TEXT,
    loan_id TEXT,
    loan_amount NUMERIC(15,2),
    loan_type VARCHAR(50),
    interest_rate NUMERIC(5,2),
    loan_term VARCHAR(50),
    approval_rejection_date DATE,
    loan_status VARCHAR(50),
    card_id TEXT,
    card_type VARCHAR(50),
    credit_limit NUMERIC(15,2),
    credit_card_balance NUMERIC(15,2),
    minimum_payment_due NUMERIC(15,2),
    payment_due_date DATE,
    last_credit_card_payment_date DATE,
    rewards_points INT,
    feedback_id TEXT,
    feedback_date DATE,
    feedback_type VARCHAR(50),
    resolution_status VARCHAR(50),
    resolution_date DATE,
    anomaly BOOLEAN
);
